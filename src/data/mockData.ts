// Helper to generate simulated history
export const generateHistory = (startPrice: number, points: number = 60, volatility: number = 0.02) => {
    let current = startPrice * 0.8; // Start lower to show growth
    const history = [];
    for (let i = 0; i < points; i++) {
        const change = (Math.random() * volatility * 2) - volatility + (0.005); // Slight upward drift
        current = current * (1 + change);
        history.push(Number(current.toFixed(2)));
    }
    // Ensure last point matches current price roughly (optional, but good for consistency)
    return history;
};

